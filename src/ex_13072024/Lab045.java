package ex_13072024;

public class Lab045 {
    public static void main(String[] args) {
        /* Comparison operators */
        System.out.println("Value is 10 == 10:" + (10 == 10)); // == Compare true values
        System.out.println("Value is 10 >= 10:" + (10 >= 10)); // 10> 10 or 10 = 10
        System.out.println("Value is 10 <= 10:" + (10 <= 10)); // 10 < 10 or 10 = 10
        System.out.println("Value is 10 > 10:" + (10 > 10));
        System.out.println("Value is 10 < 10:" + (10 < 10));
    }
}
